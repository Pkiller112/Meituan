package edu.cn.pjh.model;

public class BeanBusiness {
	private int businessid;
	private String businessname;
	private int businessStar;
	private float avgCost;
	private int Allsell;
	public int getBusinessid() {
		return businessid;
	}
	public void setBusinessid(int businessid) {
		this.businessid = businessid;
	}
	public String getBusinessname() {
		return businessname;
	}
	public void setBusinessname(String businessname) {
		this.businessname = businessname;
	}
	public int getBusinessStar() {
		return businessStar;
	}
	public void setBusinessStar(int businessStar) {
		this.businessStar = businessStar;
	}
	public float getAvgCost() {
		return avgCost;
	}
	public void setAvgCost(float avgCost) {
		this.avgCost = avgCost;
	}
	public int getAllsell() {
		return Allsell;
	}
	public void setAllsell(int allsell) {
		Allsell = allsell;
	}
}
